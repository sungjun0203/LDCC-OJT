package com.lotte.drink;

public class DrinkPicDto {
	
	
	Integer drinkPicId;
	Integer picId;
	String drinkPicName;
	String drinkPicSrc;
	
	
	public Integer getDrinkPicId() {
		return drinkPicId;
	}
	public void setDrinkPicId(Integer drinkPicId) {
		this.drinkPicId = drinkPicId;
	}
	public Integer getPicId() {
		return picId;
	}
	public void setPicId(Integer picId) {
		this.picId = picId;
	}
	public String getDrinkPicName() {
		return drinkPicName;
	}
	public void setDrinkPicName(String drinkPicName) {
		this.drinkPicName = drinkPicName;
	}
	public String getDrinkPicSrc() {
		return drinkPicSrc;
	}
	public void setDrinkPicSrc(String drinkPicSrc) {
		this.drinkPicSrc = drinkPicSrc;
	}
	
		
}
